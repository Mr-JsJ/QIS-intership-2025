// ====================================================  FOR LOOP =============================================================
// <!-- FOR LOOP FOR PRINT NUMBERS FROM 1 TO 10 -->    
for (let i = 1; i <= 10; i++) {
    document.writeln(i + "<br>");
}


document.writeln("<br>===================================================================<br>");


// =======================================================================================================================

// <!-- FOR LOOP FOR PRINT EVEN NUMBERS -->

for (let i = 1; i <= 10; i++) {
    if (i % 2 === 0) {
        document.writeln(i + "<br>");
    }
}


document.writeln("<br>===================================================================<br>");


// =======================================================================================================================


// <!-- FOR LOOP FOR PRINT ODD NUMBERS -->

for (let i = 1; i <= 10; i++) {
    if (i % 2 !== 0) {
        document.writeln(i + "<br>");
    }
}

document.writeln("<br>===================================================================<br>");


// =======================================================================================================================


// <!-- FOR LOOP FOR PRINT NUMBERS IN REVERS ORDER-->

for (let i = 10; i >= 1; i--) {
    document.writeln(i + "<br>");
}


document.writeln("<br>===================================================================<br>");


// =======================================================================================================================

// <!-- FOR LOOP FOR SUM OF NUMBERS -->

let sum = 0;
for (let i = 1; i <= 10; i++) {
    sum += i;
}
document.writeln("SUM=" + sum);


document.writeln("<br>===================================================================<br>");


// =======================================================================================================================


for (let i = 1; i <= 10; i++) {
    document.writeln("5*" + i + "=" + (5 * i) + "<br>");
}

document.writeln("<br>===================================================================<br>");


// =======================================================================================================================


let fact = 1;
for (let i = 1; i <= 10; i++) {
    fact *= i;
}
document.writeln("FACTORIAL =" + fact);

document.writeln("<br>===================================================================<br>");


